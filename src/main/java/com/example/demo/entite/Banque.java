package com.example.demo.entite;

public class Banque {
	String nom;
	String iban;
	String ibic;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getIban() {
		return iban;
	}
	public void setIban(String iban) {
		this.iban = iban;
	}
	public String getIbic() {
		return ibic;
	}
	public void setIbic(String ibic) {
		this.ibic = ibic;
	}
	
	
}
